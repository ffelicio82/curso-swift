import UIKit

// TIPOS DE DADOS

/*
 * O Swift interpreta automaticamente o tipo da variável, mas o tipo de dado também pode ser apontado explicitamente
 */

// Tipo implícito
let int = 1 // Int -> inteiros
let double = 3.14 // Double -> números decimais
let string = "Fernando" // String -> armazenar textos
let isDriver = true // -> Bool -> true ou false

// Tipo explicíto
let number : Int = 1 // Int -> inteiros
let pi : Double = 3.14 // Double -> números decimais
let name : String = "Fernando" // String -> armazenar textos
let no : Bool = false // -> Bool -> true ou false
